#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int a;
	int nb;

	i = 0;
	a = 1;
	nb = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		i++;
		if (str[i - 1] == '-')
			a = -1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (nb * a);
}
