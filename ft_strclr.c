#include "libft.h"

void	ft_strclr(char *s)
{
	size_t i;
	size_t n;

	if (s == NULL)
		return ;
	i = 0;
	n = 0;
	while (s[n])
		n++;
	while (i <= n)
	{
		s[i] = 0;
		i++;
	}
}
